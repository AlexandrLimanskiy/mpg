# –ü–æ–ª—É—á–∏—Ç—å —ç—Ñ—Ñ–µ–∫—Ç—ã —Ä–µ–≥–∏–æ–Ω–∞
def get_region_effects(country, region_id):
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —ç—Ñ—Ñ–µ–∫—Ç—ã —Ä–µ–≥–∏–æ–Ω–∞"""
    region = get_region_info(country, region_id)
    return region['effects'] if region else {}

# –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞—Ç—å –±–æ–Ω—É—Å/—à—Ç—Ä–∞—Ñ –≤ –ø—Ä–æ—Ü–µ–Ω—Ç–∞—Ö –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è
def format_effect(effect_value):
    """–§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –∑–Ω–∞—á–µ–Ω–∏–µ —ç—Ñ—Ñ–µ–∫—Ç–∞ –≤ –ø—Ä–æ—Ü–µ–Ω—Ç–∞—Ö"""
    if effect_value > 0:
        return f"+{int(effect_value * 100)}%"
    elif effect_value < 0:
        return f"{int(effect_value * 100)}%"
    else:
        return "0%"

# –ü–æ–ª—É—á–∏—Ç—å —ç–º–æ–¥–∑–∏ –∏ –∏–º—è —Ä–µ–≥–∏–æ–Ω–∞
def get_region_emoji_and_name(country, region_id):
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —ç–º–æ–¥–∑–∏ –∏ –∏–º—è —Ä–µ–≥–∏–æ–Ω–∞"""
    region = get_region_info(country, region_id)
    if not region:
        return "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π —Ä–µ–≥–∏–æ–Ω"
    return f"{region['emoji']} {region['name']}"

